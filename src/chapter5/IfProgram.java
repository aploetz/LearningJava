package chapter5;

public class IfProgram {

	public static void main(String[] args) {
		int intA = 16;
		
		if (intA == 5) {
			System.out.println("The variable intA equals 5.");
		} else if (intA > 10) {
			System.out.println("The variable intA is greater than 10.");
		} else {
			System.out.println("The variable intA is NOT equal to 5.");
		}

	}

}
